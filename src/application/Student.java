package application;

public class Student {
	public String name;
	public int status;
	public String department;
	public String cycle;
	public String qualification;
	public int student_id;
	public Student(String name, int status, String department, String cycle, String qualification, int student_id) {
		super();
		this.name = name;
		this.status = status;
		this.department = department;
		this.cycle = cycle;
		this.qualification = qualification;
		this.student_id = student_id;
	}
}
